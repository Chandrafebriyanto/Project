# roundNumbers adalah sebuah program yang digunakan untuk membulatkan angka kepada jumlah desimal tertentu.
# Ini berguna dalam berbagai aplikasi, seperti perhitungan keuangan, statistik, dan lain sebagainya.

print(round(4.567))     # 5
print(round(4.567, 2))  # 4.57
print(round(456.78, -1))  # 460


# EXAMPLE:
num1 = float(input())
num2 = int(input())

print(round(num1, num2))