# swapping variabel adalah proses menukar nilai antara dua variabel.
# Ini sering digunakan dalam algoritma dan pemrograman untuk mengubah urutan nilai.

# example:
a = 10
b = 20
a, b = b, a
print(a)  # Output: 20
print(b)  # Output: 10


# tittle: Swapping Variables Example
# Initialize variables x and y
x = 5
y = 10

# Swap the values of x and y
x, y = y , x

# Print the swapped values
print(f"x: {x}")
print(f"y: {y}")