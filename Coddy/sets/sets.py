# set adalah tipe data yang digunakan untuk menyimpan koleksi elemen yang unik, tidak berurutan, dan tidak dapat diubah
# set didefinisikan dengan tanda {} dan untuk kosong menggunakan set()

# Creating a set of numbers
numbers = {1, 2, 3, 4, 5}
print(numbers)

# Creating a set of strings
fruits = {"apple", "banana", "cherry"}
print(fruits)

# Creating an empty set
empty_set = set()
print(empty_set)

numbers = {1,1,2,3,4}
